package br.com.acervotcc.modelo;

public class Autor {
	
	//commen
	private int autId;
	private String autNome;
	private String autSobreNome;
	public int getAutId() {
		return autId;
	}
	public void setAutId(int autId) {
		this.autId = autId;
	}
	public String getAutNome() {
		return autNome;
	}
	public void setAutNome(String autNome) {
		this.autNome = autNome;
	}
	public String getAutSobreNome() {
		return autSobreNome;
	}
	public void setAutSobreNome(String autSobreNome) {
		this.autSobreNome = autSobreNome;
	}

}
